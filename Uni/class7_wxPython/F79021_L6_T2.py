class HangmanGame(wx.Frame):

    def __init__(self, *args, **kwargs):
        super(HangmanGame, self).__init__(*args, **kwargs)
        self.initUI()

    def initUI(self):
        self.mainPanel = wx.Panel(self)
        self.SetSize(wx.Size(400, 500))
        self.Show(True)

        self.initializeInput('ABCDEFGHIJKLMNOPQRSTUVWXYZ')  # hardcoded input
        self.initializeWordFromFile("./changeMe.txt")  # hardcoded file - use new line as delimiter!

        self.guessButton = wx.Button(self.mainPanel, label="Guess", pos=(150, 400))
        self.guessButton.Bind(wx.EVT_BUTTON, self.OnGuess)

        # Initial stage image
        self.hangmanImgStage = wx.StaticBitmap(self.mainPanel, 0, embeddedImages.initialStage.GetBitmap(),
                                               pos=(130, 220))

        self.livesLeft = 7
        self.livesLeftText = wx.StaticText(self.mainPanel, pos=(150, 100),
                                           label="You have " + str(self.livesLeft) + " tries left!")

    def initializeInput(self, inputLetters):

        self.letterInputList = []

        startX = 90
        startY = 150
        offsetX = 0
        offsetY = 0

        for letter in inputLetters:
            toggleButton = wx.ToggleButton(self.mainPanel, label=letter, pos=(startX + offsetX, startY + offsetY),
                                           size=(20, 20))
            toggleButton.Bind(wx.EVT_TOGGLEBUTTON, self.OnInputToggle)
            self.letterInputList.append(toggleButton)
            offsetX += 20
            if offsetX >= 220:
                offsetY += 20
                offsetX = 0

    def initializeWordFromFile(self, fileDir):

        myFile = open(fileDir)
        wordsList = [line.replace("\n", "") for line in myFile.readlines()]
        self.wordToGuess = random.choice(wordsList)

        self.wordLetters = []
        self.wordFont = wx.Font(20, wx.DECORATIVE, wx.ITALIC, wx.NORMAL)
        startX = 100
        offset = 25
        self.firstLetter = wx.StaticText(self.mainPanel, pos=(startX, 20), label=self.wordToGuess[0])
        self.firstLetter.SetFont(self.wordFont)
        self.wordLetters.append(self.firstLetter)

        for indx in range(1, len(self.wordToGuess) - 1):
            newTextEl = wx.StaticText(self.mainPanel, pos=(startX + offset, 20), label="_")
            newTextEl.SetFont(self.wordFont)
            self.wordLetters.append(newTextEl)
            offset += 25

        self.lastLetter = wx.StaticText(self.mainPanel, pos=(startX + offset, 20),
                                        label=self.wordToGuess[len(self.wordToGuess) - 1])
        self.lastLetter.SetFont(self.wordFont)
        self.wordLetters.append(self.lastLetter)

    def OnInputToggle(self, e):
        element = e.GetEventObject()
        self.guessLetter = element.GetLabel()

    def OnGuess(self, e):

        foundOccurancesIndexList = [i for i, letter in enumerate(self.wordToGuess[1:-1]) if letter == self.guessLetter]

        if foundOccurancesIndexList:
            # print "found occurances at", foundOccurancesIndexList
            for index in foundOccurancesIndexList:
                self.wordLetters[index + 1].SetLabelText(self.guessLetter)

            lettersleft = len(
                [letterTextField for letterTextField in self.wordLetters[1:-1] if letterTextField.GetLabel() == "_"])
            if (lettersleft == 0):
                wx.StaticText(self.mainPanel, label="YOU WON. Restart the game to play again!", pos=(90, 100))
                self.guessButton.Disable()
        else:
            self.livesLeft -= 1
            self.livesLeftText.SetLabelText("You have " + str(self.livesLeft) + " tries left!")
            self.hangmanImgStage.SetBitmap(getattr(embeddedImages, "stage" + str(7 - self.livesLeft)).GetBitmap())

            if self.livesLeft == 0:
                self.livesLeftText.Hide()
                wx.StaticText(self.mainPanel, label="YOU LOST. Restart the game to play again!", pos=(90, 100))
                self.guessButton.Disable()


